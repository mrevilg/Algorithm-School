# List of commonly used Standard Libaries in Python

atexit # Allows you to register functions for your program to call when exiting.

argparse # Provides functions for parsing command line arguments.

bisect # provides bisection algorithms for sorting lists.

calendar # Provides a number of date related functions.

codecs # Provides functions for encoding and decoding data.

collections # Provides a variety of useful data structures.

copy # Provides functions for copying data.

csv # Provides functions for reading and writinf CSV files.

datetime # Provides classes for handling dates and times

fnmatch # Provides functions for matching Unix style filename patterns

concurrent # Provides asynchronus computation

glob # Provides functions for matching unix style path patterns 

io # Provides fucntions handling of I/O files

json # Provides functions for reading and writing in JSON formatting

logging # Provides access to Python's own built-in logging faunctionality

multiprocessing # Allows you to run multiple subprocesses, whilst providing a thread-looking API

operator # Provides functions implementing the basic Python operators in place of own lamda's

os # Provides access to basic OS functions

random # Provides functions for providing sudorandom numbers

re # Provides regular expression functionality

sched # Provides an event scheduler without multithreading

select # Provides access to select() and poll() functions for creating event loops

shutil # Provides access to high level file functions

signal # Provides functions for dealing with POSIX signals

tempfile # Provides functions for temporary files and dictionaries

threading # provides access to high-level threading functionality

urllib # Provides functins for parsing and handling URLs

uuid # Allows you to generate universally unique identifiers (UUIDs)